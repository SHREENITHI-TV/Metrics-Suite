    private int parseIntField(javax.swing.JTextField tf) {
        String s = tf.getText().trim();
        if (s.isEmpty()) {
            return 0;
        }
        int v = Integer.parseInt(s);
        if (v < 0) {
            throw new NumberFormatException();
        }
        return v;
    }

    private double parseDoubleField(javax.swing.JTextField tf) {
        String s = tf.getText().trim();
        if (s.isEmpty()) {
            return 0.0;
        }
        double v = Double.parseDouble(s);
        if (v < 0) {
            throw new NumberFormatException();
        }
        return v;
    }

    private void recomputeUCPBaseValues() {

        // Actor counts
        int simpleActors = parseIntField(jTextActorSimpleCount);
        int averageActors = parseIntField(jTextActorAverageCount);
        int complexActors = parseIntField(jTextActorComplexCount);

        // Use case counts
        int simpleUC = parseIntField(jTextUseCaseSimpleCount);
        int averageUC = parseIntField(jTextUseCaseAverageCount);
        int complexUC = parseIntField(jTextUseCaseComplexCount);

        // Actor totals
        int actorSimpleTotal = simpleActors * 1;
        int actorAverageTotal = averageActors * 2;
        int actorComplexTotal = complexActors * 3;

        int uaw = actorSimpleTotal + actorAverageTotal + actorComplexTotal;

        // Use case totals
        int ucSimpleTotal = simpleUC * 5;
        int ucAverageTotal = averageUC * 10;
        int ucComplexTotal = complexUC * 15;

        int uucw = ucSimpleTotal + ucAverageTotal + ucComplexTotal;

        int uucp = uaw + uucw;

        // Update UI
        jTextActorSimpleTotal.setText(String.valueOf(actorSimpleTotal));
        jTextActorAverageTotal.setText(String.valueOf(actorAverageTotal));
        jTextActorComplexTotal.setText(String.valueOf(actorComplexTotal));

        jTextUseCaseSimpleTotal.setText(String.valueOf(ucSimpleTotal));
        jTextUseCaseAverageTotal.setText(String.valueOf(ucAverageTotal));
        jTextUseCaseComplexTotal.setText(String.valueOf(ucComplexTotal));

        jTextFieldUAWTotal.setText(String.valueOf(uaw));
        jTextFieldUUCWTotal.setText(String.valueOf(uucw));
        jTextFieldUUCP.setText(String.valueOf(uucp));
    }

