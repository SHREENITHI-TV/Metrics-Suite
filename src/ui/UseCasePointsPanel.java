    private void jButtonComputeUCPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComputeUCPActionPerformed
        try {
            recomputeUCPBaseValues();

            double tcf = parseDoubleField(jTextFieldTCF);
            double ecf = parseDoubleField(jTextFieldECF);
            double productivity = parseDoubleField(jTextFieldProductivityFactor);
            double locPerPM = parseDoubleField(jTextFieldLOCPerPM);
            double locPerUCP = parseDoubleField(jTextFieldLOCPerUCP);

            double uucp = Double.parseDouble(jTextFieldUUCP.getText().trim());

            double totalUCP = uucp * tcf * ecf;
            double estimatedHours = totalUCP * productivity;
            double estimatedLOC = totalUCP * locPerUCP;
            double estimatedPM = (locPerPM == 0) ? 0 : estimatedLOC / locPerPM;

            jTextFieldTotalUCP.setText(String.format("%,.2f", totalUCP));
            jTextFieldEstimatedHours.setText(String.format("%,.2f", estimatedHours));
            jTextFieldEstimatedLOC.setText(String.format("%,.2f", estimatedLOC));
            jTextFieldEstimatedPM.setText(String.format("%,.2f", estimatedPM));

        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Enter valid non-negative numbers.",
                    "Invalid input",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButtonComputeUCPActionPerformed

