    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// If no FP input data entered, ignore Compute FP per script
        if (allCountFieldsEmpty()) {
            return;
        }
        if (!vafConfirmed) {
            int choice = javax.swing.JOptionPane.showConfirmDialog(
                    this,
                    "You haven't confirmed Value Adjustments (VAF) yet.\nOpen the VAF dialog now?",
                    "VAF required",
                    javax.swing.JOptionPane.YES_NO_OPTION
            );

            if (choice == javax.swing.JOptionPane.YES_OPTION) {
                jButton2ActionPerformed(null); // opens Value Adjustments (your VAF button handler)
            }
            return;
        }
        try {
            // Ensure totals/Total Count are up-to-date
            recomputeAllTotals();

            // Read Total Count from the field recomputeAllTotals already sets
            int totalCount = 0;
            String tc = jTextField11.getText().trim();
            if (!tc.isEmpty()) {
                totalCount = Integer.parseInt(tc);
            }

            // VAF and FP
            double vaf = 0.65 + 0.01 * vafSum;     // vafSum must already be maintained elsewhere
            double fp = totalCount * vaf;

            // Display results (use your actual output fields)
            // If you have a VAF field, set it too:
            // jTextField12.setText(String.format("%,.2f", vaf));  // example
            jTextField13.setText(String.format("%,.2f", fp));
            notifyDirty();

        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Invalid Total Count. Check inputs (non-negative integers).",
                    "Invalid input",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        java.awt.Frame frame = (java.awt.Frame) javax.swing.SwingUtilities.getWindowAncestor(this);

        // Pass existing values so the dialog shows saved choices
        ValueAdjustmentDialog dlg = new ValueAdjustmentDialog(frame, true, vafValues);
        dlg.setLocationRelativeTo(this);
        dlg.setVisible(true);

        if (!dlg.isApproved()) {
            return;
        }
        vafConfirmed = true;
        // Save values back into this panel
        vafValues = dlg.getValues();
        vafSum = dlg.getSum();

        // Show VAF sum in the UI (pick the correct text field for your layout)
        jTextField14.setText(String.valueOf(vafSum));
        notifyDirty();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String fpText = jTextField13.getText().trim();
        if (fpText.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Compute FP first.");
            return;
        }

        double fpDouble;
        try {
            fpDouble = Double.parseDouble(fpText.replace(",", ""));
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "FP value is invalid.");
            return;
        }

        int fp = (int) fpDouble;   // truncate decimal part
        String lang = (currentLanguage == null || currentLanguage.isBlank()) ? "None" : currentLanguage;
        int loc = locPerFp(lang);
        if (loc < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a valid language first.");
            return;
        }

        double size = fp * loc;

        // Show code size in jTextField15
        jTextField15.setText(String.format("%,.0f", size));
        notifyDirty();
    }//GEN-LAST:event_jButton3ActionPerformed

