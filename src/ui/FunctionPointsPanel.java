    private int parseCount(javax.swing.JTextField tf) {
        String s = tf.getText().trim();
        if (s.isEmpty()) {
            return 0;
        }
        int v = Integer.parseInt(s);
        if (v < 0) {
            throw new NumberFormatException();
        }
        return v;
    }

    private void recomputeAllTotals() {
        // EI
        int ei = parseCount(jTextField1);
        int w1 = jRadioButton1.isSelected() ? 3 : jRadioButton2.isSelected() ? 4 : 6;
        int t1 = ei * w1;
        jTextField6.setText(String.valueOf(t1));

        // EO
        int eo = parseCount(jTextField2);
        int w2 = jRadioButton4.isSelected() ? 4 : jRadioButton5.isSelected() ? 5 : 7;
        int t2 = eo * w2;
        jTextField7.setText(String.valueOf(t2));

        // EQ
        int eq = parseCount(jTextField3);
        int w3 = jRadioButton7.isSelected() ? 3 : jRadioButton8.isSelected() ? 4 : 6;
        int t3 = eq * w3;
        jTextField8.setText(String.valueOf(t3));

        // ILF
        int ilf = parseCount(jTextField4);
        int w4 = jRadioButton10.isSelected() ? 7 : jRadioButton11.isSelected() ? 10 : 15;
        int t4 = ilf * w4;
        jTextField9.setText(String.valueOf(t4));

        // EIF
        int eif = parseCount(jTextField5);
        int w5 = jRadioButton13.isSelected() ? 5 : jRadioButton14.isSelected() ? 7 : 10;
        int t5 = eif * w5;
        jTextField10.setText(String.valueOf(t5));

        // Total Count
        jTextField11.setText(String.valueOf(t1 + t2 + t3 + t4 + t5));
    }

