    private boolean handlingValidationPopup = false;

    private void safeRecompute(boolean showPopup) {
        try {
            recomputeAllTotals();
        } catch (NumberFormatException ex) {
            if (!showPopup) {
                return;
            }

            // prevent double popups / re-entrance
            if (handlingValidationPopup) {
                return;
            }
            handlingValidationPopup = true;
            try {
                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "Enter non-negative integers only (blank = 0).",
                        "Invalid input",
                        javax.swing.JOptionPane.ERROR_MESSAGE
                );
            } finally {
                handlingValidationPopup = false;
            }
        }
    }

