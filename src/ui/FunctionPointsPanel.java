    private static int parseNonNegInt(String s) {
        if (s == null) {
            return 0;
        }
        s = s.trim();
        if (s.isEmpty()) {
            return 0;
        }
        int v = Integer.parseInt(s);
        return Math.max(v, 0);
    }

    private static int getSelectedWeight(javax.swing.JRadioButton r1, int v1,
            javax.swing.JRadioButton r2, int v2,
            javax.swing.JRadioButton r3, int v3,
            int defaultVal) {
        if (r1.isSelected()) {
            return v1;
        }
        if (r2.isSelected()) {
            return v2;
        }
        if (r3.isSelected()) {
            return v3;
        }
        return defaultVal;
    }

    private static void setSelectedWeight(int val,
            javax.swing.JRadioButton r1, int v1,
            javax.swing.JRadioButton r2, int v2,
            javax.swing.JRadioButton r3, int v3,
            int defaultVal) {
        // clear first
        r1.setSelected(false);
        r2.setSelected(false);
        r3.setSelected(false);

        if (val == v1) {
            r1.setSelected(true);
        } else if (val == v2) {
            r2.setSelected(true);
        } else if (val == v3) {
            r3.setSelected(true);
        } else {
            // fallback to default
            if (defaultVal == v1) {
                r1.setSelected(true);
            } else if (defaultVal == v2) {
                r2.setSelected(true);
            } else if (defaultVal == v3) {
                r3.setSelected(true);
            }
        }
    }

